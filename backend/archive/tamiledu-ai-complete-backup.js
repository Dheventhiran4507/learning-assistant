app.post('/api/questions/submit-test', (req, res) => {
  const { answers } = req.body;
  let correct = 0, wrong = 0, totalMarks = 0, earnedMarks = 0;
  const results = answers.map(a => {
    const q = DB.questions.find(x => x.id === a.questionId);
    if (!q) return null;
    const isCorrect = q.correctAnswer === a.selectedAnswer;
    if (isCorrect) { correct++; earnedMarks += q.marks; }
    else wrong++;
    totalMarks += q.marks;
    return { questionId: q.id, question: q.question, selectedAnswer: a.selectedAnswer, correctAnswer: q.correctAnswer, isCorrect, explanation: q.explanation, marks: q.marks };
  }).filter(Boolean);

  const total = correct + wrong;
  const pct = total ? (correct / total * 100).toFixed(1) : 0;
  const grade = pct >= 90 ? 'A+' : pct >= 80 ? 'A' : pct >= 70 ? 'B+' : pct >= 60 ? 'B' : pct >= 50 ? 'C' : pct >= 40 ? 'D' : 'F';
  res.json({ success: true, data: { correct, wrong, total, percentage: pct, grade, earnedMarks, totalMarks, results } });
});

