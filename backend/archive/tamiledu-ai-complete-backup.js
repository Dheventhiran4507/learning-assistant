function generateTextbookQuestions(subjectCode, unitNumber) {
  const subject = TEXTBOOKS[subjectCode];
  if (!subject) return [];

  const unit = subject.units[unitNumber];
  if (!unit) return [];

  const allQuestions = [];

  unit.topics.forEach(topic => {
    const questions = generate50FromTopic(subjectCode, unitNumber, topic, subject);
    allQuestions.push(...questions);
  });

  return allQuestions;
}

function generate50FromTopic(subjectCode, unitNumber, topic, subject) {
  const questions = [];
  const kp = topic.keyPoints;

  // Generate diverse questions from keyPoints
  kp.forEach((point, idx) => {
    const words = point.split(' ');
    const subject_name = words.slice(0, 3).join(' ');

    // Question type 1: Direct definition (Easy)
    questions.push(makeQ(subjectCode, unitNumber, topic.name, 'easy',
      `According to "${subject.textbook}", what does the following statement describe: "${point.substring(0, 60)}..."?`,
      point.substring(0, 40),
      [`A wrong concept about ${topic.name}`,
       `An unrelated term in ${topic.name}`,
       `Incorrect definition of ${words[0]}`]
    ));

    // Question type 2: Concept MCQ (Medium)
    questions.push(makeQ(subjectCode, unitNumber, topic.name, 'medium',
      `As per "${subject.author}" textbook, which statement correctly explains "${words[0]} ${words[1] || ''}" in the context of ${topic.name}?`,
      point,
      [`${words[0]} ${words[1] || ''} is not related to ${topic.name}`,
       `${words[0]} works in the opposite manner in ${topic.name}`,
       `${words[0]} only applies to advanced topics not ${topic.name}`]
    ));

    // Question type 3: Application (Hard)
    if (idx % 2 === 0) {
      questions.push(makeQ(subjectCode, unitNumber, topic.name, 'hard',
        `In "${subject.textbook}" Chapter ${subject.units[unitNumber].chapter}, which best describes the practical significance of: "${point.substring(0, 55)}..."?`,
        `It is a fundamental concept in ${topic.name} as defined in ${subject.author}'s textbook`,
        [`It is an optional concept with no real significance`,
         `It contradicts the basic principles of ${topic.name}`,
         `It applies only in theoretical scenarios not practical`]
      ));
    }
  });

  // Fill up to 50 questions with variations
  let i = 0;
  while (questions.length < 50) {
    const kpItem = kp[i % kp.length];
    const w = kpItem.split(' ');
    const difficulty = questions.length < 20 ? 'easy' : questions.length < 40 ? 'medium' : 'hard';
    questions.push(makeQ(subjectCode, unitNumber, topic.name, difficulty,
      `From the textbook "${subject.textbook}" by ${subject.author}, which option correctly relates to "${w[0]} ${w[1] || w[0]}" as explained in ${topic.name}?`,
      kpItem.substring(0, 50) + '...',
      [`Incorrect interpretation of ${w[0]}`,
       `${w[0]} has no role in ${topic.name}`,
       `This concept belongs to a different unit`]
    ));
    i++;
  }

  return questions.slice(0, 50);
}

function makeQ(subjectCode, unitNumber, topicName, difficulty, question, correctText, wrongs) {
  const options = [correctText, ...wrongs].map((text, idx) => ({
    key: String.fromCharCode(65 + idx), text
  }));
  // Shuffle options
  for (let i = options.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [options[i], options[j]] = [options[j], options[i]];
  }
  const correctKey = options.find(o => o.text === correctText)?.key || 'A';
  const optMap = {};
  options.forEach(o => { optMap[o.key] = o.text; });

  return {
    id: 'Q' + Date.now() + Math.random().toString(36).substr(2, 6),
    subjectCode, unitNumber: parseInt(unitNumber),
    topic: topicName, question, options: optMap,
    correctAnswer: correctKey,
    explanation: `As per the textbook "${TEXTBOOKS[subjectCode]?.textbook}" by ${TEXTBOOKS[subjectCode]?.author}: ${correctText}`,
    difficulty, marks: difficulty === 'easy' ? 1 : difficulty === 'medium' ? 2 : 3
  };
}

